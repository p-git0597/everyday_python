# Operations in Tuples:
# Problem 1
# Perform concatenation of two tuples and print the result.
tuple_1 = ("The", "food", "is", "delicious")
tuple_2 = ("OR", "the", "food", "was", "not", "so", "delicious")

my_tuple = tuple_1 + tuple_2
print(my_tuple)
